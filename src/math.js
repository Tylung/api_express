
/**
 * function to add 2 numbers
 * @param {number} n1 - first number
 * @param {number} n2 - second number
 * @returns {number} - result of add
 */
const add = (n1, n2) => {
    return n1 + n2
} 

const subs = (n1, n2) => {
    return n1 - n2
} 

const multiply = (n1, n2) => {
    return n1 * n2
} 

const divide = (n1, n2) => {
    return n1 / n2
} 


export {
    add,
    subs,
    multiply,
    divide
}